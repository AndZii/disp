package com.limosys.ws.obj.displine;

import com.limosys.ws.obj.Ws_Address;

public class Ws_DispLineInfo {

	private int id;
	private String name;
	private String desc;
	private Ws_Address address;
	private String status;
	private double incentiveAmount;
	private int carReqCount;
	private boolean open;
	private String colorRgb;
	private int bidCount;
	
	public Ws_DispLineInfo() {	
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the desc
	 */
	public String getDesc() {
		return desc;
	}

	/**
	 * @param desc the desc to set
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}

	/**
	 * @return the address
	 */
	public Ws_Address getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(Ws_Address address) {
		this.address = address;
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}

	/**
	 * @return the incentiveAmount
	 */
	public double getIncentiveAmount() {
		return incentiveAmount;
	}

	/**
	 * @param incentiveAmount the incentiveAmount to set
	 */
	public void setIncentiveAmount(double incentiveAmount) {
		this.incentiveAmount = incentiveAmount;
	}

	/**
	 * @return the carReqCount
	 */
	public int getCarReqCount() {
		return carReqCount;
	}

	/**
	 * @param carReqCount the carReqCount to set
	 */
	public void setCarReqCount(int carReqCount) {
		this.carReqCount = carReqCount;
	}

	/**
	 * @return the open
	 */
	public boolean isOpen() {
		return open;
	}

	/**
	 * @param open the open to set
	 */
	public void setOpen(boolean open) {
		this.open = open;
	}

	/**
	 * @return the colorRgb
	 */
	public String getColorRgb() {
		return colorRgb;
	}

	/**
	 * @param colorRgb the colorRgb to set
	 */
	public void setColorRgb(String colorRgb) {
		this.colorRgb = colorRgb;
	}

	/**
	 * @return the bidCount
	 */
	public int getBidCount() {
		return bidCount;
	}

	/**
	 * @param bidCount the bidCount to set
	 */
	public void setBidCount(int bidCount) {
		this.bidCount = bidCount;
	}
	
	

}
