package com.limosys.ws.obj.param;

public class Ws_GetPaymentsParam {
	private String deviceId;
	private int custId;
	private boolean showDetails;
	private boolean showReqData = true;

	public Ws_GetPaymentsParam(String deviceId, int custId, boolean showDetails, boolean showReqData) {
		this.setDeviceId(deviceId);
		this.setCustId(custId);
		this.setShowDetails(showDetails);
		this.setShowReqData(showReqData);
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public int getCustId() {
		return custId;
	}

	public void setCustId(int custId) {
		this.custId = custId;
	}

	public boolean isShowDetails() {
		return showDetails;
	}

	public void setShowDetails(boolean showDetails) {
		this.showDetails = showDetails;
	}

	public boolean isShowReqData() {
		return showReqData;
	}

	public void setShowReqData(boolean showReqData) {
		this.showReqData = showReqData;
	}
}
