package com.limosys.ws.obj.param;

import com.limosys.ws.obj.payment.Ws_Payment;

public class Ws_SavePaymentParam {
	private Ws_Payment payment;
	private String deviceId;
	private int custId;

	public Ws_SavePaymentParam(Ws_Payment payment, String deviceId, int custId) {
		this.setPayment(payment);
		this.setDeviceId(deviceId);
		this.setCustId(custId);
	}

	public Ws_Payment getPayment() {
		return payment;
	}

	public void setPayment(Ws_Payment payment) {
		this.payment = payment;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public int getCustId() {
		return custId;
	}

	public void setCustId(int custId) {
		this.custId = custId;
	}

}
