package com.limosys.ws.obj.param;

public class Ws_AddCompanyAccountParam {

	private String compId;
	private String deviceId;
	private int custId;
	private String acctDispId;
	private String password;
	private boolean isDefault;

	public Ws_AddCompanyAccountParam(String compId, String deviceId, int custId, String acctDispId, String password, boolean isDefault) {
		this.setCompId(compId);
		this.setDeviceId(deviceId);
		this.setCustId(custId);
		this.setAcctDispId(acctDispId);
		this.setPassword(password);
		this.setDefault(isDefault);
	}

	public String getCompId() {
		return compId;
	}

	public void setCompId(String compId) {
		this.compId = compId;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public int getCustId() {
		return custId;
	}

	public void setCustId(int custId) {
		this.custId = custId;
	}

	public String getAcctDispId() {
		return acctDispId;
	}

	public void setAcctDispId(String acctDispId) {
		this.acctDispId = acctDispId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public boolean isDefault() {
		return isDefault;
	}

	public void setDefault(boolean isDefault) {
		this.isDefault = isDefault;
	}
}
