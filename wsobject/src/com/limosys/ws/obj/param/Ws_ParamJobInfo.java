package com.limosys.ws.obj.param;

public class Ws_ParamJobInfo {

	private int jobId;
	private String deviceId;
	private boolean extraCoords;
	private boolean getCoordList = false;

	public Ws_ParamJobInfo(int jobId, String deviceId, boolean extraCoords, boolean getCoordList) {
		setJobId(jobId);
		setDeviceId(deviceId);
		setExtraCoords(extraCoords);
		this.getCoordList = getCoordList;
	}

	public int getJobId() {
		return jobId;
	}

	public void setJobId(int jobId) {
		this.jobId = jobId;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public boolean isExtraCoords() {
		return extraCoords;
	}

	public void setExtraCoords(boolean extraCoords) {
		this.extraCoords = extraCoords;
	}

	public boolean isGetCoordList() {
		return getCoordList;
	}

	public void setGetCoordList(boolean getCoordList) {
		this.getCoordList = getCoordList;
	}

}
