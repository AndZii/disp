package com.limosys.ws.obj.param;

import java.util.Date;

public class Ws_ParamJobList {

	private int custId;
	private String deviceId;
	private int historyJobs = 0;
	private Date maxJobDtm;	

	public Ws_ParamJobList() {
	}

	public Ws_ParamJobList(int custId, String deviceId) {
		this.custId = custId;
		this.deviceId = deviceId;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public int getCustId() {
		return custId;
	}

	public void setCustId(int custId) {
		this.custId = custId;
	}

	public int getHistoryJobs() {
		return historyJobs;
	}

	public void setHistoryJobs(int historyJobs) {
		this.historyJobs = historyJobs;
	}

	public Date getMaxJobDtm() {
		return maxJobDtm;
	}

	public void setMaxJobDtm(Date maxJobDtm) {
		this.maxJobDtm = maxJobDtm;
	}

}
