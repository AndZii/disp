package com.limosys.ws.obj.payment;

import com.limosys.ws.obj.Ws_Base;

public class Ws_SavePaymentResult extends Ws_Base {
	private String paymentType;
	private boolean isDefault = false;
	private int fopSeqNum;
	private String displayStr;

	public Ws_SavePaymentResult(String paymentType, boolean isDefault, int fopSeqNum, String displayStr) {
		setPaymentType(paymentType);
		setDefault(isDefault);
		setFopSeqNum(fopSeqNum);
		setDisplayStr(displayStr);
	}

	public Ws_SavePaymentResult() {}

	public String getPaymentType() {
		return paymentType;
	}

	public void setPaymentType(String paymentType) {
		this.paymentType = paymentType;
	}

	public boolean isDefault() {
		return isDefault;
	}

	public void setDefault(boolean isDefault) {
		this.isDefault = isDefault;
	}

	public int getFopSeqNum() {
		return fopSeqNum;
	}

	public void setFopSeqNum(int fopSeqNum) {
		this.fopSeqNum = fopSeqNum;
	}

	public String getDisplayStr() {
		return displayStr;
	}

	public void setDisplayStr(String displayStr) {
		this.displayStr = displayStr;
	}

}
